    private void eliminarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarjButtonActionPerformed

        PuntoventaGrupo grupoSeleccionado = obtenerGrupoSeleccionado();

        String mensaje = "Va a eliminar un grupo. Si confirma, "
                + "las categorias del grupo no quedaran ligadas a algún grupo"
                + "\n¿Desea continuar?";
        String titulo = "¿Desea continuar?";

        if (grupoSeleccionado != null) {

            int respuesta = JOptionPane.showConfirmDialog(this, mensaje, titulo, JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (respuesta == JOptionPane.YES_OPTION) {
                em.getTransaction().begin();
                em.remove(grupoSeleccionado);
                em.getTransaction().commit();
                refrescarGrupos();
            }
        }
    }//GEN-LAST:event_eliminarjButtonActionPerformed

