    private void inicializar() {

        inicarJPA();
        refrescarGrupos();
    }

    private void inicarJPA() {

        emf = Persistence.createEntityManagerFactory("PuntoDeVentaPU");
        em = emf.createEntityManager();
    }

    private void refrescarGrupos() {

        eliminarFilasGruposJTable();

        for (PuntoventaGrupo grupo : cargarGruposSQL()) {

            adicionarFilaGruposJTable(grupo);
        }
    }

    private List<PuntoventaGrupo> cargarGruposSQL() {

        List<PuntoventaGrupo> listaGrupos;
        TypedQuery<PuntoventaGrupo> typedQuery;
        typedQuery = em.createNamedQuery(
                "PuntoventaGrupo.findAll", PuntoventaGrupo.class);
        listaGrupos = typedQuery.getResultList();
        return listaGrupos;
    }

    private void eliminarFilasGruposJTable() {

        DefaultTableModel dm = (DefaultTableModel) gruposJTable.getModel();
        int rowCount = dm.getRowCount();
        //Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
    }

    private void adicionarFilaGruposJTable(PuntoventaGrupo grupo) {

        DefaultTableModel modelo = (DefaultTableModel) gruposJTable.getModel();
        modelo.addRow(new Object[]{
            grupo.getIdGrupo(),
            grupo
        });
    }

    private PuntoventaGrupo obtenerGrupoSeleccionado() {

        PuntoventaGrupo grupoSeleccionado = null;
        try {

            int idSelectedRow = gruposJTable.getSelectedRow();
            Object objectoSeleccionado = gruposJTable.getValueAt(idSelectedRow, 1);
            System.out.println(objectoSeleccionado);
            if (objectoSeleccionado instanceof PuntoventaGrupo) {
                grupoSeleccionado = (PuntoventaGrupo) objectoSeleccionado;
            }

        } catch (ArrayIndexOutOfBoundsException arrayIndexOutOfBoundsException) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un grupo de la lista");
        }
        return grupoSeleccionado;
    }
