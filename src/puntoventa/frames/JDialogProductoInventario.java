    private boolean valida(){
        
        boolean pasaValidacion = true;
        
        try {
            Double cantidadASumar = (Double)cantidadJSpinner.getValue();
            System.out.println(cantidadASumar);
        }
        catch ( ClassCastException castException ) {
            
            JOptionPane.showMessageDialog(this, "El formato de cantidad es inválido. Por favor ingrese unicamente dígitos.");
            return false;
        }
        
        return pasaValidacion;
    }

