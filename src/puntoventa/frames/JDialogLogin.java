    private void jFrameLoginAction() {
        
        String nombreUsuario = jTextFieldUsuario.getText();
        String password = new String(jPasswordField1.getPassword());
        
        if ( validarCampos(nombreUsuario, password) ){
            
            PuntoventaUsuario usuarioLogin = selectUsuarioSQL(nombreUsuario);
            
            if (usuarioLogin != null ){
            
                setLoginCorrecto(login(usuarioLogin, password));

                if ( isLoginCorrecto() ) {

                    this.setVisible(false);
                    setUsuario(usuarioLogin);
                } else {

                    JOptionPane.showMessageDialog(
                            this, 
                            "Usuario o contraseÃ±a incorrectas", 
                            "Credenciales incorrectas", 
                            JOptionPane.WARNING_MESSAGE);
                }
            } else {
                
                JOptionPane.showMessageDialog(
                            this, 
                            "Usuario o contraseÃ±a incorrectas", 
                            "Credenciales incorrectas", 
                            JOptionPane.WARNING_MESSAGE);
            }
            
        } else {
            
            JOptionPane.showMessageDialog(
                        this, 
                        "Debe ingresar usuario y contraseÃ±a", 
                        "Campos vacios", 
                        JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private PuntoventaUsuario selectUsuarioSQL(String usuario){
        
        CriteriaBuilder cb = getEm().getCriteriaBuilder();

        CriteriaQuery<PuntoventaUsuario> cq = cb.createQuery(PuntoventaUsuario.class);
        Root<PuntoventaUsuario> usuarios = cq.from(PuntoventaUsuario.class);

        cq.where(
                cb.equal(usuarios.get(PuntoventaUsuario_.usernameDesk),usuario )
        );

        cq.select(usuarios);

        TypedQuery<PuntoventaUsuario> q = getEm().createQuery(cq);
        List<PuntoventaUsuario> listaEmpresas = q.getResultList();
        
        if ( listaEmpresas.size() > 0 )
            return listaEmpresas.get(0);
        else
            return null;
    }
    
    private boolean login( PuntoventaUsuario usuario, String password ) {
        
        return  usuario.getPasswordDesk().equals(password) ;
    }
    
    private boolean validarCampos( String usuario, String password ) {
        
        return !(usuario.equals("") | 
                password.equals(""));
    }

