     private void obtenerCortesDeCaja() {

        TypedQuery<PuntoventaCorteCaja> typedQuery;
        typedQuery = em.createNamedQuery("PuntoventaCorteCaja.findAll", PuntoventaCorteCaja.class);

        // Date timeStamp = Calendar.getInstance().getTime();
        listaCorte = typedQuery.getResultList();
    }

    private void mostrarListaCorteHtml() {

         resumenCorteCajaJTextPane.setText("");

        String htmlTextBegin = "<html><body>";
        String htmlBodyTableHeader = "<table border=\"3\" style=\"width:100%\"><tr>"
                + "<td>NÃºmero de corte</td><td>Fecha de corte</td><td>Total</td></tr>";
        String htmlBodyTableContent = "";
        String htmlTextEnd = "</body></html>";

        int row = 1;

        for (PuntoventaCorteCaja corte : listaCorte) {

            String fechaFormat = "";
            String totalCompraFormat = String.format("$%.2f", corte.getTotalRecaudado());
   
            DateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            fechaFormat = formato.format(corte.getFechaCorte());
            htmlBodyTableContent += "<tr><td>Corte " + row + "</td>";
            htmlBodyTableContent += "<td>" + fechaFormat + "</td>";
            htmlBodyTableContent += "<td>" + totalCompraFormat + "</td></tr>";

            row++;
        }

        htmlBodyTableContent += "</table>";
        
        resumenCorteCajaJTextPane.setText(htmlTextBegin + htmlBodyTableHeader + htmlBodyTableContent + htmlTextEnd);
    }
    
