ALTER TABLE 
PUNTOVENTA_CATEGORIA 
ADD CONSTRAINT FK_GRUPO FOREIGN KEY (ID_GRUPO) 
REFERENCES PUNTOVENTA_GRUPO(ID_GRUPO)
ON DELETE CASCADE
;
